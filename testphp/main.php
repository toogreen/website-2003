<p class="center_subtitle">De quoi parle ce site&nbsp;?</p>

<p>Créé en novembre 2002, Puissance-Linux est un site spécialisé dans la rédaction d'articles et tutoriaux sur le système d'exploitation
GNU/Linux. Il a pour but d'informer sur les logiciels libres et OpenSource et d'aider les nouveaux-venus comme les plus confirmés à se
familiariser avec ce système d'exploitation.</p>

